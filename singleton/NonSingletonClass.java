class NonSingletonClass {
	private String name;
	private Integer number;


	public NonSingletonClass() {

	} 

	public void setNumber(Integer number) {
		this.number = number;
	}

	public Integer getNumber() {
		return this.number;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return this.name;
	}
}